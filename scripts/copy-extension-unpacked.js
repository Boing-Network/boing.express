/**
 * Copy a clean, loadable extension tree to `extension-unpacked/` at the repo root.
 * Use for Chrome "Load unpacked" before Web Store re-review (isolated from dev edits in `extension/`).
 *
 * Run after a build: `pnpm run build:extension:unpacked`
 * Chrome: chrome://extensions → Developer mode → Load unpacked → select the `extension-unpacked` folder.
 */

import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readdirSync,
  rmSync,
  statSync,
  writeFileSync,
} from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const extDir = path.join(root, 'extension');
const outDir = path.join(root, 'extension-unpacked');

function copyDirContentsSync(srcDir, destDir) {
  if (!existsSync(srcDir)) return;
  mkdirSync(destDir, { recursive: true });
  for (const name of readdirSync(srcDir)) {
    const srcPath = path.join(srcDir, name);
    const destPath = path.join(destDir, name);
    if (statSync(srcPath).isDirectory()) {
      copyDirContentsSync(srcPath, destPath);
    } else {
      copyFileSync(srcPath, destPath);
    }
  }
}

function shouldCopyRootEntry(name) {
  if (name.endsWith('.map') || name.endsWith('.ts')) return false;
  const fullPath = path.join(extDir, name);
  if (!existsSync(fullPath)) return false;
  if (statSync(fullPath).isDirectory()) return name === 'icons' || name === 'fonts';
  return ['.html', '.js', '.css', '.json'].includes(path.extname(name));
}

if (!existsSync(path.join(extDir, 'manifest.json'))) {
  console.error('Missing extension/manifest.json. Run: pnpm run build:extension');
  process.exit(1);
}

rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

for (const name of readdirSync(extDir)) {
  if (!shouldCopyRootEntry(name)) continue;
  const src = path.join(extDir, name);
  const dest = path.join(outDir, name);
  if (statSync(src).isDirectory()) {
    copyDirContentsSync(src, dest);
  } else {
    copyFileSync(src, dest);
  }
}

const listed = readdirSync(outDir);
const hasManifest = listed.includes('manifest.json');

writeFileSync(
  path.join(outDir, 'README-UNPACKED.txt'),
  `Boing Express — unpacked extension (pre-review / local testing)
===============================================================

This folder is generated by:  pnpm run build:extension:unpacked
Do not edit files here; they are overwritten on each build.

Load in Chrome
----------------
1. Open chrome://extensions
2. Turn on "Developer mode"
3. Click "Load unpacked"
4. Choose this folder: ${outDir}

Submitting to Chrome Web Store
-------------------------------
Use the zip workflow instead (bundles the same files):
  pnpm run zip:extension
Produces: boing-wallet-extension.zip at the repo root.

Relative path from repo root: extension-unpacked/
`,
  'utf8'
);

if (!hasManifest) {
  console.error('ERROR: manifest.json missing after copy.');
  process.exit(1);
}

console.log('Unpacked extension ready:', outDir);
console.log('Chrome → Load unpacked → select that folder.');
